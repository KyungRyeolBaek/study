import sys


input = sys.stdin.readline
N = int(input())
A = list(map(int, input().strip().split()))
B = list(map(int, input().strip().split()))
M = int(input())
C = list(map(int, input().strip().split()))

result = []
for c in C:
    save = c
    for idx, (a, b) in enumerate(zip(A, B)):
        if a == 0:
            B[idx] = save
            save = b
    result.append(str(save))
print(' '.join(result))

